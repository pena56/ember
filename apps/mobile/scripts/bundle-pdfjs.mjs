/**
 * bundle-pdfjs.mjs — build-time script that reads the pdfjs-dist build files
 * and writes them as TypeScript string literals into src/reader/pdf-js-content.ts.
 *
 * WHY: Metro resolves .mjs files as JavaScript modules, not static assets.
 * We need the raw file bytes/text to inject into a WebView. Generating a TS
 * source file at build time (checked in or generated pre-dev) is the most
 * reliable cross-platform mechanism: no custom Metro config, no expo-asset
 * URI gymnastics, no fs reads at runtime.
 *
 * Run: node scripts/bundle-pdfjs.mjs
 * Also run automatically via package.json "predev" script.
 *
 * Output: src/reader/pdf-js-content.ts (gitignored — regenerated from node_modules)
 */

import { readFileSync, writeFileSync, mkdirSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, '..');

const pdfJsPath = resolve(root, 'node_modules/pdfjs-dist/build/pdf.mjs');
const pdfWorkerPath = resolve(root, 'node_modules/pdfjs-dist/build/pdf.worker.mjs');
const outDir = resolve(root, 'src/reader');
const outFile = resolve(outDir, 'pdf-js-content.ts');

mkdirSync(outDir, { recursive: true });

const pdfJs = readFileSync(pdfJsPath, 'utf8');
const pdfWorker = readFileSync(pdfWorkerPath, 'utf8');

// Escape backticks and backslashes for template literal embedding
function escapeTemplateString(s) {
  return s.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${');
}

const output = `// AUTO-GENERATED by scripts/bundle-pdfjs.mjs — do not edit manually.
// Regenerate with: node scripts/bundle-pdfjs.mjs
// This file embeds the pdfjs-dist@6.0.227 build files as string constants so
// they can be injected into a react-native-webview without file URI resolution.

/** pdfjs-dist/build/pdf.mjs — the main pdf.js library */
export const PDF_JS_SRC = \`${escapeTemplateString(pdfJs)}\`;

/** pdfjs-dist/build/pdf.worker.mjs — the pdf.js worker */
export const PDF_WORKER_SRC = \`${escapeTemplateString(pdfWorker)}\`;
`;

writeFileSync(outFile, output, 'utf8');
console.log(`[bundle-pdfjs] written ${outFile}`);
console.log(`  pdf.mjs:        ${(pdfJs.length / 1024).toFixed(0)}KB`);
console.log(`  pdf.worker.mjs: ${(pdfWorker.length / 1024).toFixed(0)}KB`);
