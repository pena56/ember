// Handshake: the ~3MB pdf.js module above has finished evaluating and our
// message listener is now attached. Tell RN it is safe to post load/mode/theme.
// react-native-webview postMessage is NOT queued, so RN MUST wait for this
// before posting, otherwise the initial load is dropped and the reader hangs
// on the spinner forever.
postToRN({ type: 'bootReady' });

function handleMessage(event) {
  let msg;
  try { msg = JSON.parse(event.data); } catch { return; }

  switch (msg.type) {
    case 'load': {
      loadPdf(msg.bytesBase64);
      break;
    }
    case 'setMode': {
      if (msg.mode === mode) break;
      // Store the requested mode even before the doc loads, so loadPdf's
      // applyMode(mode) builds the correct initial view (e.g. opening in paged).
      mode = msg.mode;
      if (pdfDoc) applyMode(mode);
      break;
    }
    case 'setTheme': {
      document.documentElement.setAttribute('data-reader-theme', msg.theme);
      break;
    }
    case 'gotoPage': {
      if (!pdfDoc) break;
      const p = Math.max(1, Math.min(numPages, msg.page));
      if (mode === 'paged') {
        currentPage = p;
        renderingPages.clear();
        buildPagedPage();
        postToRN({ type: 'page', current: currentPage });
        // offset is ignored in paged mode (whole page in view, resume to page top)
      } else {
        // Scroll mode: restore to the saved fractional offset within the page.
        // pageEl.offsetTop gives the absolute scroll position of the page top;
        // adding offset * offsetHeight lands the saved fraction at the viewport top.
        const el = document.querySelector('#scroll-container [data-page="' + p + '"]');
        if (el) {
          const offset = typeof msg.offset === 'number' ? Math.min(1, Math.max(0, msg.offset)) : 0;
          const targetY = el.offsetTop + offset * el.offsetHeight;
          window.scrollTo({ top: targetY, behavior: 'smooth' });
        }
      }
      break;
    }
    case 'setAnnotations': {
      // Rebuild the per-page annotation map, then repaint all already-rendered pages.
      // Lazily virtualized pages pick up the annotations in paintAnnotations at the
      // END of renderPage, so no explicit queuing needed.
      annotationsByPage = new Map();
      var items = msg.items || [];
      for (var i = 0; i < items.length; i++) {
        var it = items[i];
        var bucket = annotationsByPage.get(it.page) || [];
        bucket.push(it);
        annotationsByPage.set(it.page, bucket);
      }
      // Repaint already-rendered page-wraps immediately.
      var rendered = document.querySelectorAll('.page-wrap[data-page]');
      for (var r = 0; r < rendered.length; r++) {
        var pageNum = parseInt(rendered[r].dataset.page, 10);
        paintAnnotations(pageNum, rendered[r]);
      }
      break;
    }
    case 'clearSelection': {
      // RN asks the WebView to drop the active DOM selection (after highlight created).
      if (window.getSelection) window.getSelection().removeAllRanges();
      break;
    }
  }
}

// ── Load PDF from base64 bytes ──
async function loadPdf(bytesBase64) {
  try {
    postToRN({ type: 'stage', stage: 'decoding' });
    // Decode base64 → Uint8Array
    const binaryStr = atob(bytesBase64);
    const bytes = new Uint8Array(binaryStr.length);
    for (let i = 0; i < binaryStr.length; i++) bytes[i] = binaryStr.charCodeAt(i);

    postToRN({ type: 'stage', stage: 'getDocument' });
    pdfDoc = await pdfjsLib.getDocument({ data: bytes }).promise;
    numPages = pdfDoc.numPages;
    currentPage = 1;
    pendingPageInScroll = 1;
    renderingPages.clear();
    pageHeights.clear();

    postToRN({ type: 'ready', numPages });
    applyMode(mode); // build the initial view
  } catch (err) {
    postToRN({ type: 'error', message: String(err) });
  }
}
