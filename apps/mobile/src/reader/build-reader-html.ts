// ── pdf.js API — reference via the global pdfjsLib that pdf.mjs sets. Do NOT
// destructure into local consts: pdf.mjs already declares top-level bindings
// named GlobalWorkerOptions/getDocument/TextLayer in THIS module scope, so
// re-declaring them throws "Identifier GlobalWorkerOptions has already been
// declared". We use pdfjsLib dot name everywhere instead.

// Worker setup. A real module Worker from the blob URL is the fast path; if the
// WebView blocks it (older engine), fall back to workerSrc (pdf.js fake worker
// on the main thread). A broken worker is a likely cause of a hang, so we
// surface failures rather than letting it stall.
try {
  pdfjsLib.GlobalWorkerOptions.workerPort = new Worker(window.__workerSrc, { type: 'module' });
} catch (e) {
  window.__postToRN({ type: 'error', message: 'worker-spawn-failed: ' + String(e) });
  pdfjsLib.GlobalWorkerOptions.workerSrc = window.__workerSrc;
}

