/**
 * build-reader-html.ts — generates the self-contained WebView HTML for the PDF reader.
 *
 * ASSET-LOADING MECHANISM (documented per spec):
 * We use `source={{ html: buildReaderHtml(pdfJsSrc, pdfWorkerSrc) }}` with pdf.js
 * content injected as inline <script> tags. This avoids file:// URI resolution
 * differences between iOS and Android in Expo Go, requires no custom Metro config,
 * and keeps the reader fully offline (no network).
 *
 * pdf.js and pdf.worker.mjs content is generated at build time by
 * `scripts/bundle-pdfjs.mjs` into `src/reader/pdf-js-content.ts` (gitignored,
 * regenerated by `predev`/`prebuild`). The worker is injected as a Blob URL so
 * pdf.js can find it via `GlobalWorkerOptions.workerSrc`.
 *
 * Reader themes (paper/sepia/night) are applied via `data-reader-theme` on <html>
 * + CSS custom properties that match ui-context.md values exactly (no token pipeline
 * available inside the WebView HTML).
 *
 * Bridge messages:
 *   RN → WebView: { type:'load', bytesBase64 } | { type:'setMode', mode } |
 *                 { type:'setTheme', theme } |
 *                 { type:'gotoPage', page, offset? }
 *                   scroll mode: scrolls to pageEl.offsetTop + offset * pageEl.offsetHeight
 *                   paged mode:  offset ignored, navigates to the page
 *   WebView → RN: { type:'ready', numPages } | { type:'page', current } |
 *                 { type:'position', page, offset }  (capture signal; debounced on scroll-settle)
 *                 { type:'error', message? } |
 *                 { type:'geometry', pageNumber, viewport:{width,height}, items } |
 *                 { type:'selection', page, startChar, endChar, rect:{x,y,width,height} } |
 *                 { type:'selectionCleared' }
 *   RN → WebView (10d additions):
 *                 { type:'setAnnotations', items:[{id,page,kind,color,boxes}] } |
 *                 { type:'clearSelection' }
 */

